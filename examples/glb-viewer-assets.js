export function getDefaultSampleAsset() {
  const asset = sampleAssets[0];

  if (asset === undefined) {
    throw new Error("GLB viewer sample catalog is empty.");
  }

  return asset;
}

export function findSampleAssetById(assetId) {
  if (typeof assetId !== "string" || assetId.length === 0) {
    return null;
  }

  return sampleAssets.find((asset) => asset.id === assetId) ?? null;
}
